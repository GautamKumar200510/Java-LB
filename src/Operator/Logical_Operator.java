public class Logical_Operator {
    public static void main(String[] args) {
        int a = 8;
        int b = 5;

        System.out.println("Logical AND : " + (a>8 && b>5));
        System.out.println("Logical OR : " + (a>8 || b>5));
        System.out.println("Logical NOT : " + !( b>5 ));


        
    }

}
